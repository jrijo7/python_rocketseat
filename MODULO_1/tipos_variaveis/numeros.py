# Inteiro
numero_inteiro = 3
print("Inteiro =", numero_inteiro)
# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante =", numero_real)

# type()
print('Tipo da variável inteiro =', type(numero_inteiro))
print('Tipo da variável inteiro =', type(numero_real))

# soma +
soma = 1 + 1
print('1+1 = ', soma)

# subtração -
subtracao = 1 - 1
print('1-1 = ', subtracao)

# multiplicação *
multiplicacao = 2 * 2
print('2 x 2 = ', multiplicacao)

# divisão inteira //
divisao_inteira = 5 // 2
print('5 // 2 = ', divisao_inteira)

# divisão real /
divisao = 5 / 2
print('5 / 2 = ', divisao)
print('Tipo da variavel do resultado da divisao = ', type(divisao))

# int()
print('Valor em inteiro = ', int(divisao))

# float()
print("Valor em float = ", float(divisao))

# Módulo %
modulo = 5 % 2
print('Modulo = ', modulo)